package spring.demo.interview.sort;

/**
 * @Package: spring.demo.interview.sort
 * @ClassName: QuiklySort
 * @Description: 快排
 *              思路：选数组中间数，
 * @Author: liangxin
 * @CreateDate: 2020/3/20 9:34
 * @UpdateDate: 2020/3/20 9:34
 */
public class QuicklySort {

    public static void main(String[] args) {
        int j=0;
        int k=0;
        for(int i=0;i<100;i++){
            j=j++;
            k++;
        }
        System.out.println("j 最后值为：" + j);
        System.out.println("k 最后值为：" + k);
    }
}
